package ir.ebookline.notebook.Model;

public class ModelNotes
{
    String id;
    String title;
    String note;
    String category;
    String date;
    String pin;
    String position;
    String type;
    String picture;
    String sound;
    String extCol1;
    String extCol2;
    String extCol3;

    public ModelNotes() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }

    public String getExtCol1() {
        return extCol1;
    }

    public void setExtCol1(String extCol1) {
        this.extCol1 = extCol1;
    }

    public String getExtCol2() {
        return extCol2;
    }

    public void setExtCol2(String extCol2) {
        this.extCol2 = extCol2;
    }

    public String getExtCol3() {
        return extCol3;
    }

    public void setExtCol3(String extCol3) {
        this.extCol3 = extCol3;
    }
}
